  !--- 5. 流程控制 (K5) ---
  PURE FUNCTION GRAVITY_ALT(ALT_M) RESULT(G_MS2)
    REAL(REAL64), INTENT(IN) :: ALT_M
    REAL(REAL64) :: G_MS2
    REAL(REAL64), PARAMETER :: G0 = 9.80665_REAL64
    REAL(REAL64), PARAMETER :: EARTH_R = 6371000.0_REAL64

    G_MS2 = G0 * (EARTH_R / (EARTH_R + ALT_M))**2
    RETURN
  END FUNCTION GRAVITY_ALT

  !--- 5. 流程控制 (K5) ---
  SUBROUTINE FLY_AIRCRAFT(SELF, DT, THROTTLE)
    CLASS(AIRCRAFT_TYPE), INTENT(INOUT) :: SELF
    REAL(REAL64), INTENT(IN) :: DT, THROTTLE
    REAL(REAL64) :: RHO, Q, LIFT, DRAG, THRUST_FORCE, G
    REAL(REAL64) :: CL, CD, ACCEL, NEW_ALT, NEW_IAS
    INTEGER :: I, HIST_IDX
    LOGICAL :: STALL_WARNING

    !--- 5. 流程控制 (K5) ---
    IF (.NOT. SELF%AIRBORNE) THEN  ! (K5) IF
      IF (SELF%IAS_MS > 50.0_REAL64) THEN  ! (K5) THEN
        SELF%AIRBORNE = .TRUE.
      ELSE
        SELF%IAS_MS = SELF%IAS_MS + (THROTTLE * SELF%ENGINE_THRUST_N / SELF%MASS_KG) * DT
        RETURN
      END IF  ! (K5) END IF
    END IF

    ! atmosphere (simple model)
    RHO = 1.225_REAL64 * EXP(-SELF%ALTITUDE_M / 8500.0_REAL64)

    G = GRAVITY_ALT(SELF%ALTITUDE_M)

    ! aerodynamics
    IF (SELF%IAS_MS > 1.0_REAL64) THEN
      Q = 0.5_REAL64 * RHO * SELF%IAS_MS**2
      CL = (G * SELF%MASS_KG) / (Q * SELF%WING_AREA_M2)
      CL = MAX(0.0_REAL64, MIN(CL, 1.8_REAL64))
      CD = 0.02_REAL64 + CL**2 / (3.14159_REAL64 * 8.0_REAL64)
    ELSE
      Q = 0.0_REAL64
      CL = 0.0_REAL64
      CD = 0.02_REAL64
    END IF

    LIFT = CL * Q * SELF%WING_AREA_M2
    DRAG = CD * Q * SELF%WING_AREA_M2
    THRUST_FORCE = THROTTLE * SELF%ENGINE_THRUST_N * (RHO / 1.225_REAL64)

    ! forces
    ACCEL = (THRUST_FORCE - DRAG) / SELF%MASS_KG
    SELF%IAS_MS = SELF%IAS_MS + ACCEL * DT
    NEW_ALT = SELF%ALTITUDE_M + (LIFT - G * SELF%MASS_KG) / SELF%MASS_KG * DT

    !--- 5. 流程控制 (K5) ---
    STALL_WARNING = (SELF%IAS_MS < 30.0_REAL64) .AND. SELF%AIRBORNE
    SELECT CASE (.TRUE.)  ! (K5) SELECT CASE
    CASE (NEW_ALT < 0.0_REAL64)  ! (K5) CASE
      NEW_ALT = 0.0_REAL64
      SELF%AIRBORNE = .FALSE.
      SELF%IAS_MS = 0.0_REAL64
    CASE (STALL_WARNING)  ! (K5) CASE
      NEW_ALT = NEW_ALT - 50.0_REAL64 * DT  ! stall descent
    CASE DEFAULT  ! (K5) CASE DEFAULT
      ! normal flight
    END SELECT  ! (K5) END SELECT

    SELF%ALTITUDE_M = NEW_ALT
    SELF%FUEL_KG = SELF%FUEL_KG - SELF%SFC_KG_PER_NS * THRUST_FORCE * DT
    SELF%MASS_KG = SELF%MASS_KG - SELF%SFC_KG_PER_NS * THRUST_FORCE * DT
    SELF%FLIGHT_TIME_S = SELF%FLIGHT_TIME_S + DT
    SELF%HEADING_RAD = MOD(SELF%HEADING_RAD + 0.0001_REAL64, 6.28318_REAL64)

    ! history
    HIST_IDX = MOD(INT(SELF%FLIGHT_TIME_S * 10.0_REAL64), MAX_HISTORY) + 1
    IF (ALLOCATED(SELF%HISTORY_ALT)) THEN
      SELF%HISTORY_ALT(HIST_IDX) = SELF%ALTITUDE_M
    END IF

    !--- 5. 流程控制 (K5) ---
    DO I = 1, 3  ! (K5) DO
      SELF%TRANSPONDER_CODE(I) = MOD(SELF%TRANSPONDER_CODE(I) + 1, 8)
      IF (SELF%FUEL_KG < 100.0_REAL64) EXIT  ! (K5) EXIT (emergency squawk)
    END DO  ! (K5) END DO

    !--- 5. 流程控制 (K5) ---
    DO WHILE (SELF%FUEL_KG < 0.0_REAL64)  ! (K5) DO WHILE
      SELF%FUEL_KG = 0.0_REAL64
      CYCLE  ! (K5) CYCLE (exits loop)
    END DO
  END SUBROUTINE FLY_AIRCRAFT

