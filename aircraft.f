    OPEN(NEWUNIT=UNIT_NUM, FILE='report.dat', STATUS='OLD', POSITION='APPEND')
    WRITE(UNIT_NUM, '(A,2F10.2)') 'Status: ', AC%ALTITUDE_M, AC%FUEL_KG
    !--- 6. 输入输出 (K6) ---
    BACKSPACE(UNIT_NUM)  ! (K6) BACKSPACE — undo last write
    WRITE(UNIT_NUM, '(A,2F10.2)') 'Final: ', AC%ALTITUDE_M, AC%FUEL_KG
    !--- 6. 输入输出 (K6) ---
    ENDFILE(UNIT_NUM)  ! (K6) ENDFILE
    !--- 6. 输入输出 (K6) ---
    REWIND(UNIT_NUM)  ! (K6) REWIND
    READ(UNIT_NUM, '(A)') BUFFER  ! (K6) READ
    CLOSE(UNIT_NUM)
  END SUBROUTINE FORMATTED_REPORT

