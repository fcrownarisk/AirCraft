  IMPLICIT NONE

  TYPE(AIRCRAFT_TYPE) :: AC1, AC2, AC3
  TYPE(SQUADRON_TYPE) :: SQUADRON
  TYPE(AIRPORT_TYPE) :: AIRPORT
  TYPE(ATC_CLEARANCE_TYPE) :: TOWER
  INTEGER :: STEPS
  REAL(REAL64) :: DT

  !--- 11. 杂项 (K11) ---
  ! EQUIVALENCE is obsolescent, skip explicit use; conceptually present
  INTEGER :: OLD_STYLE_ARR(10)

  ! Initialize
  AC1 = INIT_AIRCRAFT('N12345', 2500.0_REAL64, 220.0_REAL64, 30.0_REAL64, &
                       15000.0_REAL64, 1.5E-5_REAL64, 500.0_REAL64, 1)

