  !--- 6. 输入输出 (K6) ---
  SUBROUTINE FLY_SIMULATE(AC, STEPS)
    TYPE(AIRCRAFT_TYPE), INTENT(INOUT) :: AC
    INTEGER, INTENT(IN) :: STEPS
    INTEGER :: STEP
    !--- 3. 声明属性 (K3) ---
    DOUBLE PRECISION :: START_TIME  ! (K2) DOUBLE PRECISION
    REAL :: FUEL_START  ! (K2) REAL
    REAL(REAL64), PARAMETER :: DT = 0.1_REAL64

    START_TIME = 0.0D0  ! (K11) double precision constant
    FUEL_START = REAL(AC%FUEL_KG)

    !--- 6. 输入输出 (K6) ---
    OPEN(NEWUNIT=OUTPUT_UNIT, FILE='aircraft.log', STATUS='REPLACE', ACTION='WRITE')  ! (K6) OPEN
    WRITE(OUTPUT_UNIT, '(A,I0)') 'Aircraft ID: ', AC%ID  ! (K6) WRITE

    DO STEP = 1, STEPS
      CALL AC%FLY(DT, 0.7_REAL64)
      IF (MOD(STEP, 100) == 0) THEN
        WRITE(OUTPUT_UNIT, '(I6,2F10.2)') STEP, AC%ALTITUDE_M, AC%IAS_MS
      END IF
    END DO

    !--- 6. 输入输出 (K6) ---
    CLOSE(OUTPUT_UNIT)  ! (K6) CLOSE

    !--- 6. 输入输出 (K6) ---
    INQUIRE(FILE='aircraft.log', EXIST=AC%AIRBORNE)  ! (K6) INQUIRE — recycle logical

    !--- 11. 杂项 (K11) ---
    BLOCK  ! (K11) BLOCK
      REAL(REAL64) :: LOCAL_SUMMARY
      LOCAL_SUMMARY = AC%ALTITUDE_M
      PRINT *, 'Final altitude: ', LOCAL_SUMMARY  ! (K6) PRINT
    END BLOCK  ! (K11) END BLOCK
  END SUBROUTINE FLY_SIMULATE

  !--- 5. 流程控制 (K5) ---
  SUBROUTINE SQUADRON_SIMULATE(SQUAD, STEPS)
    TYPE(SQUADRON_TYPE), INTENT(INOUT) :: SQUAD
    INTEGER, INTENT(IN) :: STEPS
    INTEGER :: STEP, J
    REAL(REAL64) :: DT

    DT = 0.1_REAL64
    DO J = 1, SIZE(SQUAD%AIRCRAFT)
      DO STEP = 1, STEPS
        CALL SQUAD%AIRCRAFT(J)%FLY(DT, 0.7_REAL64)
        !--- 5. 流程控制 (K5) ---
        IF (SQUAD%AIRCRAFT(J)%FUEL_KG <= 0.0_REAL64) THEN
          WRITE(OUTPUT_UNIT, '(A,I0,A)') 'Aircraft ', J, ' out of fuel'
          EXIT  ! (K5) EXIT
        END IF
      END DO
    END DO

    !--- 8. 并行 (K8) ---
    SYNC ALL  ! (K8) SYNC ALL (meaningful in coarray context)
  END SUBROUTINE SQUADRON_SIMULATE

